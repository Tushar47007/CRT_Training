my_string = 'Hello!, World'
print(my_string[5])
print(my_string[-1])

#string slicing
print(my_string[0:5])
print(my_string[0:6])
print(my_string[5:len(my_string)]) #from ! to end
print(my_string[:10])
print(my_string[:])
print(my_string[0::2])#gap of 1
print(my_string[1::2])
print(my_string[0::3])#gap of 2
print(my_string[::-1])#Reverse order
print(my_string[:5:-1])



